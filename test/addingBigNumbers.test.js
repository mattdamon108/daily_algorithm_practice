const answer = require("../addingBigNumbers");

describe("Add big numbers", () => {
  it('"1" + "1" -> "2"', () => {
    expect(answer.add("1", "1")).toBe("2");
  });

  it('"123" + "456" -> "579"', () => {
    expect(answer.add("123", "456")).toBe("579");
  });

  it('"1372" + "69" -> "1441"', () => {
    expect(answer.add("1372", "69")).toBe("1441");
  });

  it('"63829983432984289347293874" + "90938498237058927340892374089" -> "91002328220491911630239667963"', () => {
    expect(
      answer.add("63829983432984289347293874", "90938498237058927340892374089")
    ).toBe("91002328220491911630239667963");
  });
});
